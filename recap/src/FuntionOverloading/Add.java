package FuntionOverloading;

public class Add {


    static double addNumber(double n1, double n2) {
        return n1 + n2;
    }
    static double addNumber(double n1, double n2, double n3) {
        return n1 + n2 + n3;
    }
    private static int addNumber(int  n1,int n2){
        return  n1 + n2;
    }
}
